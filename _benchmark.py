"""
Benchmark detect_pulse_2.Pulse_Detector against test_signal.iq.

test_signal.iq: complex64, 192 kHz, 1 second, 100 pulses at 2.5-5.5 kHz.
Generated by Jeff scripts/synthesize_test_signal.py with np.random.seed(0).

Reports:
  - pulses detected vs ground truth
  - false positives
  - wall-clock time vs recording duration
  - per-function timing breakdown
"""
import numpy as np
import sys, time, os
sys.path.insert(0, ".")
import detect_pulse_2 as dp2

# ── Ground truth (reconstruct from synthesis params + fixed seed) ──────────
# IMPORTANT: synthesize_test_signal.py generates noise BEFORE pulse parameters,
# so the noise RNG consumption must be replicated here to get the correct seed state.
SAMP_RATE       = 192_000
SIGNAL_DURATION = 1          # s
NUM_PULSES      = 100
PULSE_SAMPLES   = int(round(2.5 / 1000 * SAMP_RATE))   # 480

np.random.seed(0)
# Replicate noise generation to advance RNG to the same state as the synth script
_n_samples = len(np.arange(0, SIGNAL_DURATION, 1 / SAMP_RATE))
np.random.normal(size=_n_samples)   # real part of noise
np.random.normal(size=_n_samples)   # imag part of noise

ground_truth  = []   # list of (start_s, end_s, freq_hz)
for _ in range(NUM_PULSES):
    start_idx = np.random.randint(0, _n_samples - PULSE_SAMPLES + 1)
    freq      = np.random.uniform(2.5e3, 5.5e3)
    np.random.uniform(0, 2 * np.pi)   # phase (consumed but not needed)
    ground_truth.append((start_idx / SAMP_RATE,
                         (start_idx + PULSE_SAMPLES) / SAMP_RATE,
                         freq))

# ── Load IQ data ───────────────────────────────────────────────────────────
iq_path = "test_signal.iq"
if not os.path.exists(iq_path):
    sys.exit(f"ERROR: {iq_path} not found")

iq_data = np.fromfile(iq_path, dtype=np.complex64)
print(f"Loaded {len(iq_data):,} samples @ {SAMP_RATE} Hz "
      f"({len(iq_data)/SAMP_RATE:.3f} s)")

# ── Run detector in chunks ─────────────────────────────────────────────────
CHUNK_SIZE = 2048
det = dp2.Pulse_Detector(
    output_type="test",
    samp_rate=SAMP_RATE,
    verbose=False,
    pulse_len_ms=2.5,
)
n_chunks = len(iq_data) // CHUNK_SIZE

t0 = time.perf_counter()
raw_detections = []
for c in range(n_chunks):
    chunk  = iq_data[c * CHUNK_SIZE : (c + 1) * CHUNK_SIZE]
    result = det.detect([chunk])
    raw_detections.extend(result[0])
elapsed = time.perf_counter() - t0

# ── Parse detected timestamps ──────────────────────────────────────────────
detected_times = []
for d in raw_detections:
    try:
        ts = float(d.split(",")[0])
        detected_times.append(ts)
    except (IndexError, ValueError):
        pass
detected_times.sort()

# ── Match detections to ground truth (within 1 ms tolerance) ──────────────
MATCH_TOL = 1e-3   # 1 ms — same as Lucas's previous benchmark
used_det  = set()
matched   = 0
for gt_start, gt_end, _ in ground_truth:
    for j, ts in enumerate(detected_times):
        if j in used_det:
            continue
        if abs(ts - gt_start) < MATCH_TOL:
            matched += 1
            used_det.add(j)
            break

false_positives = len(detected_times) - len(used_det)

# ── Report ─────────────────────────────────────────────────────────────────
rec_s = len(iq_data) / SAMP_RATE
print()
print(f"Ground truth   : {len(ground_truth)} pulses")
print(f"Detected       : {len(detected_times)}")
print(f"Matched        : {matched} / {len(ground_truth)}")
print(f"False positives: {false_positives}")
print(f"Elapsed        : {elapsed:.2f}s  (recording = {rec_s:.2f}s)")
print(f"Realtime ratio : {elapsed/rec_s:.2f}x  (< 1.0 = faster than realtime)")
print()

total = det.times["detect_edges"]
if total > 0:
    print("Per-function breakdown (cumulative across all chunks):")
    for fn, t in sorted(det.times.items(), key=lambda x: -x[1]):
        if t > 0:
            print(f"  {fn:30s}: {t:.3f}s ({100*t/total:.1f}%)")
